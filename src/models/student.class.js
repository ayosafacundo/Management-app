export class Student {
    constructor(
        first_name,
        last_name,
        age,
        gender,
        contact,
        student_email,
        courses,
    ) {
        this.fname = first_name;
        this.lname = last_name;
        this.age = age;
        this.gender = gender;
        this.contact = contact;
        this.email = student_email;
        this.courses = courses;
    }
}